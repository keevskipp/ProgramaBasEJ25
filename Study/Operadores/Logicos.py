# AND ---> Se tiene que cumplir ambas condicones.

Resultado = True & True # Devuelve True
Resultado2 = False & True # Devuelve False
Resultado3 = True & False # Devuelve False
Resultado4 = False & False # Devuelve False

#OR ---> Devuelve True con que solo una se cumpla.

Resultado5 = True | True # Devolver True
Resultado6 = False | True #Devolver True
Resultado7 = True | False #Devolver True
