def km_a_millas(km):
    """Convierte kilómetros a millas (1 km = 0.621371 millas)"""
    return km * 0.621371

def celsius_a_fahrenheit(celsius):
    """Convierte Celsius a Fahrenheit (°F = °C × 9/5 + 32)"""
    return celsius * 9/5 + 32

def litros_a_galones(litros):
    """Convierte litros a galones (1 litro = 0.264172 galones)"""
    return litros * 0.264172